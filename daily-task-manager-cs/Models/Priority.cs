namespace daily_task_manager_cs.Models
{
    /// <summary>
    /// Enum representing task priority levels.
    /// Used to categorize tasks by importance.
    /// </summary>
    public enum Priority
    {
        /// <summary>
        /// Low priority task
        /// </summary>
        Low = 0,

        /// <summary>
        /// Medium priority task (default)
        /// </summary>
        Medium = 1,

        /// <summary>
        /// High priority task
        /// </summary>
        High = 2
    }
}